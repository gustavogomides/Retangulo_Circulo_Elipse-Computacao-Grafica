package pacotes.modelo;

public class Circulo {

	private double xc;
	private double yc;
	private int raio;

	// ****************************************************************************************
	public Circulo(double xc, double yc, int raio) {
		super();
		this.xc = xc;
		this.yc = yc;
		this.raio = raio;
	}

	// ****************************************************************************************
	public double getXc() {
		return xc;
	}

	// ****************************************************************************************
	public void setXc(double xc) {
		this.xc = xc;
	}

	// ****************************************************************************************
	public double getYc() {
		return yc;
	}

	// ****************************************************************************************
	public void setYc(double yc) {
		this.yc = yc;
	}

	// ****************************************************************************************
	public int getRaio() {
		return raio;
	}

	// ****************************************************************************************
	public void setRaio(int raio) {
		this.raio = raio;
	}
	// ****************************************************************************************
}
