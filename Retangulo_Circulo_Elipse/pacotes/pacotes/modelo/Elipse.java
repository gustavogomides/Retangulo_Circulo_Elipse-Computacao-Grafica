package pacotes.modelo;

public class Elipse {

	private double xc;
	private double yc;
	private double rx;
	private double ry;

	// ****************************************************************************************
	public Elipse(double xc, double yc, double rx, double ry) {
		super();
		this.xc = xc;
		this.yc = yc;
		this.rx = rx;
		this.ry = ry;
	}

	// ****************************************************************************************
	public double getXc() {
		return xc;
	}

	// ****************************************************************************************
	public void setXc(double xc) {
		this.xc = xc;
	}

	// ****************************************************************************************
	public double getYc() {
		return yc;
	}

	// ****************************************************************************************
	public void setYc(double yc) {
		this.yc = yc;
	}

	// ****************************************************************************************
	public double getRx() {
		return rx;
	}

	// ****************************************************************************************
	public void setRx(double rx) {
		this.rx = rx;
	}

	// ****************************************************************************************
	public double getRy() {
		return ry;
	}

	// ****************************************************************************************
	public void setRy(double ry) {
		this.ry = ry;
	}
	// ****************************************************************************************
}
